import Foundation

struct UserLogsAnalyzer: CleanupAnalyzer {
    let key = "user_logs"
    let title = "User Logs"
    let description = "Old log files from ~/Library/Logs"
    let isSafeByDefault = true

    func analyze(excludedPrefixes: [String]) async -> CleanupCategoryResult {
        let root = FileManager.default.homeDirectoryForCurrentUser.appendingPathComponent("Library/Logs")
        let items = CleanupFileEnumerator.collectFiles(in: root, olderThanDays: 14, excludedPrefixes: excludedPrefixes, confidenceScore: 0.9, explainability: "File is older than 14 days in Logs and likely reproducible.")
        return CleanupCategoryResult(key: key, title: title, description: description, isSafeByDefault: isSafeByDefault, riskLevel: .low, recommendationReason: "Old logs are usually safe to remove and regenerated by apps.", confidenceScore: 0.92, explainability: "Age-based rule + logs location + low functional impact.", items: items)
    }
}

