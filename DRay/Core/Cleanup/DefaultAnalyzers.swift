enum CleanupFileEnumerator {
    static func collectFiles(in root: URL, olderThanDays days: Int, excludedPrefixes: [String], confidenceScore: Double, explainability: String) -> [CleanupItem] {
        let fm = FileManager.default
        guard fm.fileExists(atPath: root.path) else { return [] }

        let keys: Set<URLResourceKey> = [.isDirectoryKey, .contentModificationDateKey, .fileSizeKey]
        guard let enumerator = fm.enumerator(at: root, includingPropertiesForKeys: Array(keys), options: [.skipsHiddenFiles]) else {
            return []
        }

        let cutoff = Calendar.current.date(byAdding: .day, value: -days, to: Date()) ?? .distantPast
        var result: [CleanupItem] = []

        for case let fileURL as URL in enumerator {
            let path = fileURL.path
            if excludedPrefixes.contains(where: { path == $0 || path.hasPrefix($0 + "/") }) {
                continue
            }
            guard let values = try? fileURL.resourceValues(forKeys: keys), values.isDirectory != true else { continue }
            guard let modified = values.contentModificationDate, modified < cutoff else { continue }
            let size = Int64(values.fileSize ?? 0)
            result.append(CleanupItem(url: fileURL, sizeInBytes: size, confidenceScore: confidenceScore, explainability: explainability))
        }

        return result.sorted { $0.sizeInBytes > $1.sizeInBytes }
    }

    static func collectLanguageFiles(in root: URL, excludedPrefixes: [String]) -> [CleanupItem] {
        let fm = FileManager.default
        guard fm.fileExists(atPath: root.path),
              let enumerator = fm.enumerator(at: root, includingPropertiesForKeys: [.isDirectoryKey, .fileSizeKey], options: [.skipsHiddenFiles]) else {
            return []
        }

        var result: [CleanupItem] = []
        for case let fileURL as URL in enumerator {
            let path = fileURL.path
            if excludedPrefixes.contains(where: { path == $0 || path.hasPrefix($0 + "/") }) { continue }
            guard path.contains(".lproj/") else { continue }
            guard let values = try? fileURL.resourceValues(forKeys: [.isDirectoryKey, .fileSizeKey]), values.isDirectory != true else { continue }
            result.append(CleanupItem(url: fileURL, sizeInBytes: Int64(values.fileSize ?? 0), confidenceScore: 0.5, explainability: "Localized resource file matched by .lproj path pattern."))
        }
        return result.sorted { $0.sizeInBytes > $1.sizeInBytes }
    }

    static func collectOrphanPreferenceFiles(in root: URL, excludedPrefixes: [String]) -> [CleanupItem] {
        let fm = FileManager.default
        guard fm.fileExists(atPath: root.path),
              let enumerator = fm.enumerator(at: root, includingPropertiesForKeys: [.isDirectoryKey, .fileSizeKey], options: [.skipsHiddenFiles]) else {
            return []
        }

        let installed = installedBundleIDs()
        var result: [CleanupItem] = []
        for case let fileURL as URL in enumerator {
            let path = fileURL.path
            if excludedPrefixes.contains(where: { path == $0 || path.hasPrefix($0 + "/") }) { continue }
            guard fileURL.pathExtension == "plist" else { continue }
            guard let values = try? fileURL.resourceValues(forKeys: [.isDirectoryKey, .fileSizeKey]), values.isDirectory != true else { continue }
            let bundleID = fileURL.deletingPathExtension().lastPathComponent
            guard !bundleID.isEmpty, !installed.contains(bundleID) else { continue }
            result.append(CleanupItem(url: fileURL, sizeInBytes: Int64(values.fileSize ?? 0), confidenceScore: 0.82, explainability: "Preference file bundle ID does not match installed applications."))
        }
        return result.sorted { $0.sizeInBytes > $1.sizeInBytes }
    }

    private static func installedBundleIDs() -> Set<String> {
        guard let apps = try? FileManager.default.contentsOfDirectory(
            at: URL(fileURLWithPath: "/Applications"),
            includingPropertiesForKeys: nil,
            options: [.skipsHiddenFiles]
        ) else { return [] }

        var ids = Set<String>()
        for appURL in apps where appURL.pathExtension == "app" {
            if let bundle = Bundle(url: appURL), let id = bundle.bundleIdentifier {
                ids.insert(id)
            }
        }
        return ids
    }
}
